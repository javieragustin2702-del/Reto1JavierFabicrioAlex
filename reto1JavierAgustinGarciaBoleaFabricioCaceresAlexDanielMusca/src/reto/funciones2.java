package reto;

public class funciones2 {
	public static int sumar(int a, int b) {

		return a + b;

	}

	public static int restar(int a, int b) {

		return a - b;

	}

	public static boolean esPar(int num) {
		if (num % 2 == 0) {
			return true;
		} else {
			return false;
		}

	}

	public static boolean esImpar(int num) {
		if (num % 2 != 0) {
			return true;
		} else {
			return false;
		}

	}
}
